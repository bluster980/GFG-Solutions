/*
  Reverse a linked list
  The input list will have at least one element  
  Return the node which points to the head of the new LinkedList
  Node is defined as 
    struct node
    {
        int data;
        struct node* next;
    
        node(int x){
            data = x;
            next = NULL;
        }
    
    }*head;
*/

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        // Complete this method
        struct node *t1=head;
        struct node *t2=head;
        while(t1){
            int g=k;
            t2=t1;
            vector<int> v;
            while(g-- && t1!=NULL){
                v.push_back(t1->data);
                t1=t1->next;
            }
            int i=0,j=v.size()-1;
            while(i<j){
                swap(v[i],v[j]);
                i++;j--;
            }
            for(auto it:v){
                t2->data=it;
                t2=t2->next;
            }
        }
        return head;
    }
};


